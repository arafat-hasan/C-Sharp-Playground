using System;

namespace CsharpMath
{
    class Program
    {
        public static void Main()
        {
            Console.WriteLine(Math.Max(4, 5));
            Console.WriteLine(Math.Min(4.4, 5.5));
            Console.WriteLine(Math.Sqrt(15));
            Console.WriteLine(Math.Round(4.77777777));
            Console.WriteLine(Math.Ceiling(4.8888888));
            Console.WriteLine(Math.Abs(-5.5));
        }
    }
}